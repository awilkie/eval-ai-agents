def export_convergence_to_ascii():
    try:
        print("Exporting convergence results to ASCII visualization...")
        df = pd.DataFrame(experiment["task_runs"])
        
        total_runs = len(df)
        
        outputs = df["output"].to_dict().values()
        valid_paths = [o.get('path_length') for o in outputs if o and isinstance(o, dict) and o.get('path_length') is not None]
        
        if not valid_paths:
            print("No valid path lengths found, skipping ASCII generation.")
            return
            
        optimal_len = min(valid_paths)
        avg_len = sum(valid_paths) / len(valid_paths)
        
        scores = []
        for eval_run in experiment.get("evaluation_runs", []):
            if getattr(eval_run, "name", "") == "Convergence Eval":
                result = getattr(eval_run, "result", None)
                if result and isinstance(result, dict) and "score" in result:
                    scores.append(result["score"])
                elif result and isinstance(result, (int, float)):
                    scores.append(result)

        if scores:
            avg_score = sum(scores) / len(scores)
        else:
            avg_score = 0.0
            
        score_percent = avg_score * 100
        
        path_counts = {}
        for p in valid_paths:
            path_counts[p] = path_counts.get(p, 0) + 1
            
        distribution_lines = []
        for p in sorted(path_counts.keys()):
            count = path_counts[p]
            bar = "█" * int((count / total_runs) * 40)
            distribution_lines.append(f"  {p} Steps  | {bar:<40} | {count} runs")
        
        distribution_text = "\n".join(distribution_lines)
        
        ascii_content = f"""========================================================================
                 AGENT CONVERGENCE RESULTS VISUALISATION                 
========================================================================

Based on the Convergence Evaluation:
The evaluation measures whether the agent efficiently resolves
queries by comparing its trace path length against the minimum (optimal) 
path length across multiple execution runs.

------------------------------------------------------------------------
                             METRICS OVERVIEW                           
------------------------------------------------------------------------
  Total Test Runs Analyzed:     {total_runs}
  Optimal Path Length (Min):    {optimal_len} steps
  Average Path Length:          {avg_len:.1f} steps
  Overall Convergence Score:    {avg_score:.2f} ({score_percent:.1f}%)

------------------------------------------------------------------------
                       CONVERGENCE SCORE GAUGE                          
------------------------------------------------------------------------

      0.0         0.25        0.50        0.75        1.0
       |-----------|-----------|-----------|-----------|
Score: [{"█" * int(avg_score * 47):<47}] {score_percent:.1f}%

------------------------------------------------------------------------
                      PATH LENGTH DISTRIBUTION                          
------------------------------------------------------------------------
 
{distribution_text}

========================================================================"""

        from datetime import datetime
        timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
        filename = f"convergence_visualization_ascii_{timestamp}.txt"
        with open(filename, "w") as f:
            f.write(ascii_content)
        print(f"Convergence ASCII visualization exported to {filename}")
        
    except Exception as e:
        print(f"Failed to generate ASCII visualization: {e}")

export_convergence_to_ascii()

