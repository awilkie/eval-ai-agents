# ## Evaluating the Path

# In[ ]:


outputs = [run["output"] for run in experiment["task_runs"]]

# Will include the user and system messages
valid_path_lengths = [output.get('path_length') for output in outputs if output and isinstance(output, dict) and output.get('path_length') is not None]

if valid_path_lengths:
    optimal_path_length = min(valid_path_lengths)
else:
    optimal_path_length = 1

print(f"The optimal path length is {optimal_path_length}")


# In[ ]:


@create_evaluator(name="Convergence Eval", kind="CODE")
def evaluate_path_length(output: dict) -> float:
    if output and isinstance(output, dict) and output.get("path_length"):
        return optimal_path_length/float(output.get("path_length"))
    else:
        return 0


# In[ ]:


experiment = evaluate_experiment(experiment=experiment,
                            evaluators=[evaluate_path_length])


